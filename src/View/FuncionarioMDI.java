    private void jTableFuncionariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableFuncionariosMouseClicked

        String nome = ""+jTableFuncionarios.getValueAt(jTableFuncionarios.getSelectedRow(), 1);
        conex.conexao();
        conex.executaSql("select * from funcionario where nome='"+nome+"'");
        try {
            conex.rs.first();
            jTextFieldId.setText(String.valueOf(conex.rs.getInt("id")));
            jTextFieldNomeFuncionario.setText(String.valueOf(conex.rs.getString("nome")));
            jComboBoxSexoFuncionario.setSelectedItem(String.valueOf(conex.rs.getString("sexo")));
            jComboBoxFuncao.setSelectedItem(String.valueOf(conex.rs.getString("funcao")));
            jTextFieldContactoFuncionario.setText(String.valueOf(conex.rs.getString("contacto")));
            jTextFieldNrDeIdentificacao.setText(String.valueOf(conex.rs.getString("identificacao")));
            jTextFieldFaltas.setText(String.valueOf(conex.rs.getInt("faltas")));

            jButtonExcluir.setEnabled(true);
            jButtonEditar.setEnabled(true);
            jButtonCancelar.setEnabled(true);
            jButtonRemover.setEnabled(true);
            jButtonSalvar.setEnabled(false);
            jButtonAdicionar.setEnabled(true);

            jTextFieldNomeFuncionario.setEnabled(false);
            jComboBoxSexoFuncionario.setEnabled(false);
            jComboBoxFuncao.setEnabled(false);
            jTextFieldContactoFuncionario.setEnabled(false);
            jTextFieldNrDeIdentificacao.setEnabled(false);
            jTextFieldFaltas.setEnabled(false);

            preencherTabela("select * from funcionario order by id");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "ERRO AO SELECIONAR" +ex);
        }
        conex.desconexao();
    }//GEN-LAST:event_jTableFuncionariosMouseClicked

     public void preencherTabela(String sql){
            
        ArrayList dados = new ArrayList();
        String [] colunas = new String[]{"id","Nome","Sexo","Funcao","Contacto","Identificacao","Faltas"};
        conex.conexao();
        
        conex.executaSql(sql);
            try {
                conex.rs.first();
                do{
                  dados.add(new Object[]{conex.rs.getInt("id"),conex.rs.getString("nome"),conex.rs.getString("sexo"),conex.rs.getString("funcao"),conex.rs.getString("contacto"),conex.rs.getString("identificacao"),conex.rs.getInt("faltas")});  
                }while(conex.rs.next());
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "ERRO AO PREENCHER ARRAYLIST"+ex);
            }
            
            ModeloTabela modelo = new ModeloTabela(dados, colunas);
            
            jTableFuncionarios.setModel(modelo);
            jTableFuncionarios.getColumnModel().getColumn(0).setPreferredWidth(30);
            jTableFuncionarios.getColumnModel().getColumn(0).setResizable(false);
            jTableFuncionarios.getColumnModel().getColumn(1).setPreferredWidth(227);
            jTableFuncionarios.getColumnModel().getColumn(1).setResizable(false);
            jTableFuncionarios.getColumnModel().getColumn(2).setPreferredWidth(90);
            jTableFuncionarios.getColumnModel().getColumn(2).setResizable(false);
            jTableFuncionarios.getColumnModel().getColumn(3).setPreferredWidth(65);
            jTableFuncionarios.getColumnModel().getColumn(3).setResizable(false);
            jTableFuncionarios.getColumnModel().getColumn(4).setPreferredWidth(100);
            jTableFuncionarios.getColumnModel().getColumn(4).setResizable(false);
            jTableFuncionarios.getColumnModel().getColumn(5).setPreferredWidth(114);
            jTableFuncionarios.getColumnModel().getColumn(5).setResizable(false);
            jTableFuncionarios.getColumnModel().getColumn(6).setPreferredWidth(120);
            jTableFuncionarios.getColumnModel().getColumn(6).setResizable(false);
            jTableFuncionarios.getColumnModel().getColumn(6).setPreferredWidth(80);
            jTableFuncionarios.getColumnModel().getColumn(6).setResizable(false);
            jTableFuncionarios.getTableHeader().setReorderingAllowed(false);
            jTableFuncionarios.setAutoResizeMode(jTableFuncionarios.AUTO_RESIZE_ALL_COLUMNS);
            jTableFuncionarios.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            conex.desconexao();
        }

