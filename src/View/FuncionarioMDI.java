    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed

        jTextFieldPesquisar.setText("");
        if (jTextFieldNomeFuncionario.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha O Campo 'Nome' Para Continuar!");
            jTextFieldNomeFuncionario.requestFocus();
        }else if(jTextFieldContactoFuncionario.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha O Campo 'Contacto' Para Continuar!");
            jTextFieldContactoFuncionario.requestFocus();
        }else if(jTextFieldFaltas.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha O Campo 'Faltas' Para Continuar!");
            jTextFieldFaltas.requestFocus();
        }else if(jTextFieldNrDeIdentificacao.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha O Campo 'Número De Identificação' Para Continuar!");
            jTextFieldNrDeIdentificacao.requestFocus();
        }else if(jTextFieldNrDeIdentificacao.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha O Campo 'Hora De Entrega' Para Continuar!");
            jTextFieldNrDeIdentificacao.requestFocus();

        } else if (flag==1){

            mod.setNome(jTextFieldNomeFuncionario.getText());
            mod.setContacto(jTextFieldContactoFuncionario.getText());
            mod.setSexo((String)jComboBoxSexoFuncionario.getSelectedItem());
            mod.setFaltas(Integer.valueOf(jTextFieldFaltas.getText()));
            mod.setFuncao((String)jComboBoxFuncao.getSelectedItem());
            mod.setIdentificacao(jTextFieldNrDeIdentificacao.getText());
            control.salvar(mod);

            jButtonEditar.setEnabled(false);
            jButtonCancelar.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonRemover.setEnabled(true);

            jTextFieldNomeFuncionario.setText("");
            jTextFieldContactoFuncionario.setText("");
            jTextFieldNrDeIdentificacao.setText("");
            jTextFieldFaltas.setText("");

            jComboBoxFuncao.setEnabled(!true);
            jComboBoxSexoFuncionario.setEnabled(!true);
            jTextFieldContactoFuncionario.setEnabled(!true);
            jTextFieldNomeFuncionario.setEnabled(!true);
            jTextFieldNrDeIdentificacao.setEnabled(!true);
            preencherTabela("select * from funcionario order by id");
        } else{

            jComboBoxFuncao.setEnabled(!true);
            jComboBoxSexoFuncionario.setEnabled(!true);
            jTextFieldContactoFuncionario.setEnabled(!true);
            //jTextFieldNascimentoFuncionario.setEnabled(!true);
            jTextFieldNomeFuncionario.setEnabled(!true);
            jTextFieldNrDeIdentificacao.setEnabled(!true);

            jButtonEditar.setEnabled(false);
            jButtonCancelar.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonRemover.setEnabled(true);

            mod.setId(Integer.parseInt(jTextFieldId.getText()));
            mod.setFaltas(Integer.parseInt(jTextFieldFaltas.getText()));
            mod.setNome(jTextFieldNomeFuncionario.getText());
            mod.setContacto(jTextFieldContactoFuncionario.getText());
            mod.setSexo((String)jComboBoxSexoFuncionario.getSelectedItem());
            //mod.setNascimento(Integer.valueOf(jTextFieldNascimentoFuncionario.getText()));
            mod.setFuncao((String)jComboBoxFuncao.getSelectedItem());
            mod.setIdentificacao(jTextFieldNrDeIdentificacao.getText());
            control.editar(mod);

            jTextFieldNomeFuncionario.setText("");
            jTextFieldContactoFuncionario.setText("");
            //jTextFieldNascimentoFuncionario.setText("");
            jTextFieldNrDeIdentificacao.setText("");
            jTextFieldFaltas.setText("");

            preencherTabela("select * from funcionario order by id");
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

