    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed

        jTextFieldPesquisar.setText("");
        if (jTextFieldNome.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha O Campo 'Nome Do Produto' Para Continuar!");
            jTextFieldNome.requestFocus();
        }else if(jTextFieldQuantidade.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha O Campo 'Quantidade' Para Continuar!");
            jTextFieldQuantidade.requestFocus();
        }else if(jFormattedTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha O Campo 'Valor Por Unidade' Para Continuar!");
            jFormattedTextField1.requestFocus();
        } else if (flag==1){

            mod.setNmproduto(jTextFieldNome.getText());
            mod.setQtd(Integer.valueOf(jTextFieldQuantidade.getText()));
            mod.setValor(Float.valueOf(jFormattedTextField1.getText()));
            control.salvar(mod);

            jButtonEditar.setEnabled(false);
            jButtonCancelar.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonNovo.setEnabled(true);

            jTextFieldNome.setEnabled(false);
            jFormattedTextField1.setEnabled(false);
            jTextFieldQuantidade.setEnabled(false);

            jTextFieldNome.setText("");
            jTextFieldQuantidade.setText("");
            jFormattedTextField1.setText("");
            jTextFieldId.setText("");

            preencherTabela("select * from produto order by id");

        } else{

            jTextFieldNome.setEnabled(false);
            jFormattedTextField1.setEnabled(false);
            jTextFieldQuantidade.setEnabled(false);

            jButtonEditar.setEnabled(false);
            jButtonCancelar.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonNovo.setEnabled(true);

            mod.setId(Integer.parseInt(jTextFieldId.getText()));
            mod.setNmproduto(jTextFieldNome.getText());
            mod.setQtd(Integer.valueOf(jTextFieldQuantidade.getText()));
            mod.setValor(Float.valueOf(jFormattedTextField1.getText()));
            control.editar(mod);

            jTextFieldNome.setText("");
            jTextFieldQuantidade.setText("");
            jFormattedTextField1.setText("");
            jTextFieldId.setText("");
            preencherTabela("select * from produto order by id");
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

